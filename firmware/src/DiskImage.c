void DumpMemory(const unsigned char *start, const unsigned char *end, bool showAddresses, unsigned int startAddress, bool showAscii, bool showHex, bool sectors)
{
    size_t i, j, k;
    const unsigned char *p = (unsigned char *) KVA0_TO_KVA1(start);
    size_t sectorCount = (end - start + 511) / 512;
    end = (unsigned char *) KVA0_TO_KVA1(end);
    
    char ascii[17] = "   ";
    for (i = 0; i < sectorCount; ++i)
    {
        if (sectors)
            printf("\r\n/* Sector %d */\r\n", (int) i);
        for (j = 0; j < 512 / 16 && p < end; ++j)
        {
            if (showAddresses)
            {
                printf("%04X ", startAddress);
                startAddress += 16;
            }
            for (k = 0; k < 16 && p < end; ++k, ++p)
            {
                static char text[7] = {'0', 'x', '0', '0', ',', ' ', '\0'};
                text[2] = '0' + (*p >> 4);
                if (text[2] > '9')
                    text[2] += 7;
                text[3] = '0' + (*p & 0xf);
                if (text[3] > '9')
                    text[3] += 7;
                if (showHex)
                    fputs(text, stdout);
                
                if ((*p >= ' ' && *p <= '~') || (!showHex && (*p == '\r' || *p == '\n')))
                    ascii[k] = *p;
                else
                    ascii[k] = '.';
            }
            if (showAscii)
            {
                if (showHex)
                    printf("// ");
                printf(ascii);
            }
            if (showHex)
                printf("\r\n");
        }
    }
}

// Dumps the contents of the disk from an offset of 'start' for 'maxBytes' bytes.
// Returns the next offset or -1 if done
int __attribute__((used)) DumpDisk(int start, size_t maxBytes, bool showAscii, bool showHex)
{
    size_t end = sizeof(diskImage);
    if (start == -1 || start > end)
        return -1;
    const uint8_t *src = (unsigned char *) KVA0_TO_KVA1(diskImage + start);

    if (start + maxBytes > end)
        maxBytes = end - start;
    
    DumpMemory(src, src + maxBytes, false, 0, showAscii, showHex, false);
    
    if (start + maxBytes >= sizeof(diskImage))
        return -1;
    else
        return start + maxBytes;
}


// Figures out the end of non-zero data on the disk
int __attribute__((used)) DiskNonZeroSize()
{
    size_t end = sizeof(diskImage);
    while (end > 0 && diskImage[end - 1] == 0)
        --end;
    return end;
}

